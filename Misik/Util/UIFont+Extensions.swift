import UIKit

extension UIFont {
    
    static let titleLg: UIFont          = .systemFont(ofSize: 28, weight: .bold)
    static let titleM: UIFont           = .systemFont(ofSize: 24, weight: .bold)
    static let titleSm: UIFont          = .systemFont(ofSize: 22, weight: .bold)
    
    static let bodyLg: UIFont           = .systemFont(ofSize: 16, weight: .medium)
    static let bodyM: UIFont            = .systemFont(ofSize: 15, weight: .medium)
    static let bodySm: UIFont           = .systemFont(ofSize: 14, weight: .medium)
    static let bodyXsm: UIFont          = .systemFont(ofSize: 13, weight: .medium)
    
    static let buttonPrimary: UIFont    = .systemFont(ofSize: 16, weight: .semibold)
    static let buttonSecondary: UIFont  = .systemFont(ofSize: 16, weight: .medium)
    static let buttonTertiary: UIFont   = .systemFont(ofSize: 14, weight: .medium)
}
